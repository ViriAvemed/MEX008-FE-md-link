
const readFileMd = require('./readFileMd');
const typeFile = require('./restrictFile.js');

const searchLinks =(typeFile)=> {
  const regex = /\[(.+)\]\((https?.+)\)/gm;
  let match;
  let i = 0;
  let newArray = [];
  while ((match = regex.exec(typeFile)) !== null) {
    const newObject = {
      href: match[2],
      text: match[1],
      File: process.argv[2].resolve
    };
    i++;
    newArray.push(newObject);
  }
  // console.log(newArray);
  return newArray;
  if ((match = regex.exec(typeFile)) === null) {
    console.log("No se encontró ningún link en el archivo.")
  }

};
module.exports= searchLinks;
